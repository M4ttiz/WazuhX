echo ""
echo "  ‚Ėą‚Ėą‚ēó    ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēó   ‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēó  ‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēó  ‚Ėą‚Ėą‚ēó"
echo "  ‚Ėą‚Ėą‚ēĎ    ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēó‚ēö‚ēź‚ēź‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĎ  ‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĒ‚ēĚ"
echo "  ‚Ėą‚Ėą‚ēĎ ‚Ėą‚ēó ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĎ  ‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĎ ‚ēö‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ "
echo "  ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēĎ ‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ  ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēĎ ‚Ėą‚Ėą‚ēĒ‚Ėą‚Ėą‚ēó "
echo "  ‚ēö‚Ėą‚Ėą‚Ėą‚ēĒ‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĎ  ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĎ  ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēĚ ‚Ėą‚Ėą‚ēó"
echo "   ‚ēö‚ēź‚ēź‚ēĚ‚ēö‚ēź‚ēź‚ēĚ ‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēĚ‚ēö‚ēź‚ēź‚ēź‚ēź‚ēź‚ēź‚ēĚ ‚ēö‚ēź‚ēź‚ēź‚ēź‚ēź‚ēĚ ‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēĚ‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēĚ"
echo ""
echo -e "${GREEN}WazuhX Setup${NC}"
echo ""

# Check Docker
if ! command -v docker &>/dev/null; then
  echo -e "${RED}‚úó Docker not found. Install Docker first: https://docs.docker.com/get-docker/${NC}"
  exit 1
fi
echo -e "${GREEN}‚úď Docker found${NC}"

# Check Docker Compose
if ! docker compose version &>/dev/null; then
  echo -e "${RED}‚úó Docker Compose v2 not found.${NC}"
  exit 1
fi
echo -e "${GREEN}‚úď Docker Compose found${NC}"

# Check wazuh-net
if ! docker network inspect wazuh-net &>/dev/null; then
  echo -e "${YELLOW}‚ö† Docker network 'wazuh-net' not found.${NC}"
  echo -e "  Creating it now (you can attach your Wazuh stack to it manually)..."
  docker network create wazuh-net
  echo -e "${GREEN}‚úď Created wazuh-net${NC}"
else
  echo -e "${GREEN}‚úď wazuh-net network exists${NC}"
fi

# .env setup
if [ ! -f .env ]; then
  cp .env.example .env
  echo -e "${YELLOW}‚úď Created .env from .env.example ‚ÄĒ please edit it before starting.${NC}"
  echo ""
  echo "  Required values to set in .env:"
  echo "    WAZUH_API_URL      ‚Üí your Wazuh manager URL (default: https://wazuh-manager:55000)"
  echo "    WAZUH_USER         ‚Üí Wazuh API user"
  echo "    WAZUH_PASSWORD     ‚Üí Wazuh API password"
  echo "    WAZUH_INDEXER_URL  ‚Üí Wazuh Indexer URL"
  echo "    GEMINI_API_KEY     ‚Üí Google Gemini API key (https://aistudio.google.com/app/apikey)"
  echo ""
  echo -e "${YELLOW}  Edit .env then re-run: docker compose up --build -d${NC}"
else
  echo -e "${GREEN}‚úď .env already exists${NC}"
  echo ""
  echo -e "${GREEN}Starting WazuhX...${NC}"
  docker compose up --build -d
  echo ""
  echo -e "${GREEN}‚úÖ WazuhX is running at http://localhost:3000${NC}"
fi
